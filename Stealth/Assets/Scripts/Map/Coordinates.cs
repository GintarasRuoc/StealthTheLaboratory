// Holds coordinates of tiles
public class Coordinates
{
    private int x, z;

    public Coordinates()
    {

    }

    public Coordinates(int x, int z)
    {
        this.x = x;
        this.z = z;
    }

    public int getX()
    {
        return x;
    }

    public int getZ()
    {
        return z;
    }
}
